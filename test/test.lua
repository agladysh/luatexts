  ensure_returns(
      "nil tail garbage ignored " .. NAME,
      2, { true, nil },
      luatexts.load('1' .. NL .. '-' .. NL .. "yada yada!")
    )

  ensure_returns(
      "false " .. NAME,
      2, { true, false },
      luatexts.load('1' .. NL .. '0' .. NL)
    )

  ensure_returns(
      "true " .. NAME,
      2, { true, true },
      luatexts.load('1' .. NL .. '1' .. NL)
    )

  ensure_returns(
      "true-nil-false " .. NAME,
      4, { true, true, nil, false },
      luatexts.load(
          '3' .. NL
       .. '1' .. NL
       .. '-' .. NL
       .. '0' .. NL
        )
    )

  print("===== END core tests", NAME, "=====")
  print("===== BEGIN uint tests", NAME, "=====")

  ensure_returns(
      "zero uint " .. NAME,
      2, { true, 0 },
      luatexts.load(
          '1' .. NL
       .. 'U' .. NL
         .. '0' .. NL
        )
    )

  ensure_returns(
      "one uint " .. NAME,
      2, { true, 1 },
      luatexts.load(
          '1' .. NL
       .. 'U' .. NL
         .. '1' .. NL
        )
    )

  -- Exact error message is implementation detail
  ensure_error_with_substring(
      "negative uint " .. NAME,
      "load failed: value too huge",
      luatexts.load(
          '1' .. NL
       .. 'U' .. NL
         .. '-1' .. NL
        )

          '1' .. NL
       .. '8' .. NL
         .. (NL == "\r\n" and '3' or '2') .. NL
         .. 'A' .. NL
        )
    )

  for i = 1, 1e3 do
    local b = { }
    for i = 1, 1024 do
      b[#b + 1] = "A"
